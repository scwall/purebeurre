
class Products:
    def __init__(self,name,description,nutrition_grade,shop,link_http):
        self.name = name
        self.description = description
        self.nutrition_grade = nutrition_grade
        self.shop = shop
        self.link_http = link_http
        def get_name(self):
            return self.name
        def get_description(self):
            return self.description
        def get_nutrition_grade(self):
            return self.nutrition_grade
        def get_shop(self):
            return self.shop
        def get_link_http(self):
            return self.link_http

